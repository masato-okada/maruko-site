<script>
(function() {
  const searchInput = document.getElementById('maker-search');
  const allCategories = document.querySelectorAll('.maker-category');
  const allMakers = document.querySelectorAll('.maker-item');

  if (searchInput) {
    searchInput.addEventListener('input', function() {
      const searchTerm = this.value.toLowerCase().trim();
      
      allCategories.forEach(function(category) {
        let hasVisibleItems = false;
        const makers = category.querySelectorAll('.maker-item');
        
        makers.forEach(function(maker) {
          const makerName = maker.getAttribute('data-maker').toLowerCase();
          
          if (searchTerm === '' || makerName.includes(searchTerm)) {
            maker.style.display = '';
            hasVisibleItems = true;
          } else {
            maker.style.display = 'none';
          }
        });
        
        // カテゴリ全体を表示/非表示
        if (hasVisibleItems || searchTerm === '') {
          category.style.display = '';
        } else {
          category.style.display = 'none';
        }
      });
    });
  }
})();
</script>

